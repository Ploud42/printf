#include "libft.h"

void	ft_putnbr(int n)
{
	long	i;
	int		k;
	int		a;

	k = 1;
	i = n;
	i = n < 0 ? -i : i;
	if (n < 0)
		ft_putchar('-');
	while (k <= (i / 10))
		k = k * 10;
	while (k >= 1)
	{
		a = i / k + 48;
		i = i % k;
		ft_putchar(a);
		k = k / 10;
	}
}
